int	ft_sqrt(int nb)
{
	int i;

	i = 1;
	while(i <= nb / 2)
	{
		if (nb == i * i)
			return (i);
		i++;
	}
	return (0);
}

#include <stdio.h>

int main()
{
	printf("%d\n", ft_sqrt(26));
}